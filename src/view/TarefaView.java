    private void atualizarLista() {
        DefaultListModel modelo = new DefaultListModel();
        for (TarefaModel t : controller.getLista()) {
            modelo.addElement(t);
        }
        jList1.setModel(modelo);
        jlTotal.setText(
                "Total de tarefas: "
                + controller.totalTarefas()
        );
    }
    
    
