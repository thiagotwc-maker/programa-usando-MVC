    private void bttCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttCadastroActionPerformed
        String nome = jTnome.getText();
        boolean adicionou = controller.adicionarTarefa(nome);
        if (adicionou) {
            atualizarLista();
            jTnome.setText("");
        } else {
            JOptionPane.showMessageDialog(null,
                    "Digite uma tarefa válida ou não repetida.");
        }
    }//GEN-LAST:event_bttCadastroActionPerformed

    private void bttConcluidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttConcluidaActionPerformed
        int indice = jList1.getSelectedIndex();
        controller.concluirTarefa(indice);
        atualizarLista();

    }//GEN-LAST:event_bttConcluidaActionPerformed

    private void bttApagarTarefaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttApagarTarefaActionPerformed
        int indice = jList1.getSelectedIndex();
        controller.removerTarefa(indice);
        atualizarLista();

    }//GEN-LAST:event_bttApagarTarefaActionPerformed

